DOMAIN= echo -n "Enter Your Domain ex:indo.qql.icu: "
read DOMAIN
SUB_DOMAIN=$ echo -n "Enter Your Host/subdomain again: "
            read SUB_DOMAIN
NS_DOMAIN=$ echo -n "Enter Your SLOWDNSHOST/Sub in subdomain ex:dns.indo.qqgl.cu: "
              read NS_DOMAIN
CF_ID=eb9851ec9c1f1597dffa745472e33984
CF_KEY=65eed4ccfa099a23b912627ec0190c4356d05
set -euo pipefail
IP=$(wget -qO- icanhazip.com);
echo "Updating DNS for ${SUB_DOMAIN}..."
ZONE=$(curl -sLX GET "https://api.cloudflare.com/client/v4/zones?name=${DOMAIN}&status=active" \
     -H "X-Auth-Email: ${CF_ID}" \
     -H "X-Auth-Key: ${CF_KEY}" \
     -H "Content-Type: application/json" | jq -r .result[0].id)

RECORD=$(curl -sLX GET "https://api.cloudflare.com/client/v4/zones/${ZONE}/dns_records?name=${SUB_DOMAIN}" \
     -H "X-Auth-Email: ${CF_ID}" \
     -H "X-Auth-Key: ${CF_KEY}" \
     -H "Content-Type: application/json" | jq -r .result[0].id)

